
#ifndef __PROJECT2_PHONG_SHADER_H__
#define __PROJECT2_PHONG_SHADER_H__

#ifdef _MSC_VER
#include <windows.h>
#endif // _MSC_VER

#include "GL/glew.h"
#include "GL/gl.h"

#include "Shader.h"

#include <string >

namespace Msg
{
  namespace MsgCore
  {
    class Node;
  }
}

namespace Project2 
{  
  /////////////////////////////////////////////////////////////////////////////
  //
  // Class that implement Phong Shading. 
  // @Note: Requires vertex and fragment shaders. 
  //
  ///////////////////////////////////////////////////////////////////////////// 

  class PhongShader : public Shader
  {
    public: 

      /////////////////////////////////////////////////////////////////////////
      //
      // Constructor. 
      // 
      /////////////////////////////////////////////////////////////////////////

      PhongShader();

      
      /////////////////////////////////////////////////////////////////////////
      //
      // Return id for this shader.  
      // 
      /////////////////////////////////////////////////////////////////////////

      std::string   id() const;


      /////////////////////////////////////////////////////////////////////////
      //
      // Initialize any non context specific variables here. 
      // 
      /////////////////////////////////////////////////////////////////////////

      virtual void  init();

    
      /////////////////////////////////////////////////////////////////////////
      //
      // Initialize any context specific variables here. 
      // 
      /////////////////////////////////////////////////////////////////////////

      virtual void contextInit();


      /////////////////////////////////////////////////////////////////////////
      //
      // Parse arguments. 
      // 
      // @Note: This is a substitute for config as of now. 
      //
      /////////////////////////////////////////////////////////////////////////

      virtual void  parseArguments( std::vector< std::string >& arguments );

     
      /////////////////////////////////////////////////////////////////////////
      //
      // Verify if the state is dirty and perform re-initialization.
      // 
      /////////////////////////////////////////////////////////////////////////

      virtual void reset();


      /////////////////////////////////////////////////////////////////////////
      //
      // Activate and apply the shader. 
      // 
      /////////////////////////////////////////////////////////////////////////

      virtual void  activate( Msg::MsgCore::Node* node );


      /////////////////////////////////////////////////////////////////////////
      //
      // De-activate the shader. 
      // 
      /////////////////////////////////////////////////////////////////////////

      virtual void  deActivate( Msg::MsgCore::Node* node );


      /////////////////////////////////////////////////////////////////////////
      //
      // Set vertex and fragment shaders. 
      // 
      /////////////////////////////////////////////////////////////////////////

      void          setShaderFiles( const std::string& vert, const std::string& frag );

      
      /////////////////////////////////////////////////////////////////////////
      //
      // Compile and load shaders. 
      // 
      /////////////////////////////////////////////////////////////////////////

      void          compileAndLink();


    protected: 

      /////////////////////////////////////////////////////////////////////////
      //
      // Destructor.  
      // 
      /////////////////////////////////////////////////////////////////////////
      
      virtual ~PhongShader();
  };  
}

#endif // __PROJECT2_PHONG_SHADER_H__
